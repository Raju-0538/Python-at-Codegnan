print("Bitwise Operators")
print("Operator Left Shift : ")
print("10<<2 is ",10<<2)
print("10<<3 is ",10<<3)
print("45<<4 is ",45<<4)
print("Operator Right Shift : ")
print("10>>2 is ",10>>2)
print("10>>3 is ",10>>3)
print("45>>4 is ",45>>4)
print("Operator Bitwise and : ")
print("14&7 is ",10&2)
print("10&3 is ",10&3)
print("45&4 is ",45&4)
print("Operator Bitwise or : ")
print("14|7 is ",10|2)
print("10|3 is ",10|3)
print("45|4 is ",45|4)
print("Operator Bitwise xor : ")
print("14^7 is ",10^2)
print("10^3 is ",10^3)
print("45^4 is ",45^4)
